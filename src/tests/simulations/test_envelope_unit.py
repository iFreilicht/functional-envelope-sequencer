"""Unit tests for simulations.envelope.

Each test focuses on one specific property. Irrelevant parameters are
generated via faker so every run acts as a light fuzz pass. Explicit
edge-case values (0.0, boundaries, etc.) are never generated by faker
in tests that are meant to cover ordinary behaviour - that way the edge
cases live in their own dedicated tests and the fuzz tests stay clean.
"""

import pytest
from faker import Faker
from simulations.envelope import (
    AMPLITUDE_LOWER_CUTOFF,
    AMPLITUDE_MAX,
    AMPLITUDE_MIN,
    INTERVAL_MAX,
    PROGRESS_MAX,
    PROGRESS_MIN,
    SHAPE_MAX,
    SHAPE_MIN,
    SLOPE_TIME_MIN,
    TIME_END,
    TIME_MIDPOINT,
    TIME_START,
    EnvelopeSettings,
    EnvelopeStatus,
    a_d_envelope,
    a_d_shape,
    combine_envelopes,
    combine_interpolate_linear,
    combine_max,
    offset_envelopes,
)

fake = Faker()

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _random_shape() -> float:
    """Random shape strictly inside valid range (excludes boundary values)."""
    return fake.pyfloat(min_value=0.001, max_value=0.999)


def _random_progress() -> float:
    """Random progress strictly inside valid range (excludes 0 and 1)."""
    return fake.pyfloat(min_value=0.001, max_value=0.999)


def _random_attack_decay() -> float:
    """Random attack or decay strictly inside (0, TIME_MIDPOINT)."""
    return fake.pyfloat(min_value=0.05, max_value=TIME_MIDPOINT - 0.05)


def _random_amplitude_enabled() -> float:
    """Random amplitude that is clearly enabled (well above the cutoff)."""
    return fake.pyfloat(min_value=AMPLITUDE_LOWER_CUTOFF + 0.05, max_value=0.95)


def _settings(
    *,
    attack: float | None = None,
    decay: float | None = None,
    shape: float | None = None,
    amplitude: float | None = None,
) -> EnvelopeSettings:
    """Build an EnvelopeSettings, using faker for any unspecified field."""
    return EnvelopeSettings(
        attack=attack if attack is not None else _random_attack_decay(),
        decay=decay if decay is not None else _random_attack_decay(),
        shape=shape if shape is not None else _random_shape(),
        amplitude=amplitude if amplitude is not None else _random_amplitude_enabled(),
    )


def _status(
    time: float, value: float, midpoint: float = TIME_MIDPOINT
) -> EnvelopeStatus:
    return EnvelopeStatus(time=time, midpoint=midpoint, value=value)


