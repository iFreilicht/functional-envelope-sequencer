class TestADEnvelopeZeroRegions:
    def test_zero_before_attack_window(self):
        attack = fake.pyfloat(min_value=0.1, max_value=TIME_MIDPOINT - 0.01)
        settings = _settings(attack=attack)
        # Pick a time that is safely before the attack window opens
        attack_start = TIME_MIDPOINT - attack
        if attack_start <= TIME_START:
            pytest.skip("attack fills entire pre-midpoint region for this random value")
        time = fake.pyfloat(min_value=TIME_START, max_value=attack_start - 0.001)
        assert a_d_envelope(settings, time) == 0.0

    def test_zero_after_decay_window(self):
        decay = fake.pyfloat(min_value=0.1, max_value=TIME_MIDPOINT - 0.01)
        settings = _settings(decay=decay)
        decay_end = TIME_MIDPOINT + decay
        if decay_end >= TIME_END:
            pytest.skip("decay fills entire post-midpoint region for this random value")
        time = fake.pyfloat(min_value=decay_end + 0.001, max_value=TIME_END)
        assert a_d_envelope(settings, time) == 0.0

    def test_zero_at_time_start(self):
        # With attack well inside the window TIME_START is guaranteed to be silent
        settings = _settings(attack=fake.pyfloat(min_value=0.1, max_value=0.8))
        assert a_d_envelope(settings, TIME_START) == 0.0

    def test_zero_at_time_end(self):
        settings = _settings(decay=fake.pyfloat(min_value=0.1, max_value=0.8))
        assert a_d_envelope(settings, TIME_END) == 0.0


