class TestADEnvelopeAmplitudeScaling:
    def test_amplitude_scales_linearly(self):
        # Use a time that is guaranteed to land inside the envelope window.
        # Attack and decay each span 0.3 units, so the window is [0.7, 1.3].
        attack = 0.3
        decay = 0.3
        shape = _random_shape()
        # Pick a time inside the window (not exactly at midpoint to avoid
        # the trivial case where both values are just amplitude).
        time = fake.pyfloat(
            min_value=TIME_MIDPOINT - attack + 0.05, max_value=TIME_MIDPOINT - 0.05
        )

        amplitude_a = fake.pyfloat(
            min_value=AMPLITUDE_LOWER_CUTOFF + 0.05, max_value=0.45
        )
        amplitude_b = amplitude_a * 2.0

        v_a = a_d_envelope(
            _settings(attack=attack, decay=decay, shape=shape, amplitude=amplitude_a),
            time,
        )
        v_b = a_d_envelope(
            _settings(attack=attack, decay=decay, shape=shape, amplitude=amplitude_b),
            time,
        )

        assert v_a > 0.0, "Expected non-zero value at chosen time"
        assert v_b / v_a == pytest.approx(2.0)


